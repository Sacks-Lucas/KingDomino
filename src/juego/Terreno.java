package juego;

public class Terreno {
	private String tipo;
	private int cantCoronas;
	private int valor;
	
	public Terreno(String tipo, int cantCoronas,int valor) {
		this.tipo = tipo;
		this.valor = valor;
		this.cantCoronas = cantCoronas;
	}

	public int getTipo() {
		
		return this.valor;
	}
	
	
}
